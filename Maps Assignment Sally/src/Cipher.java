public class Cipher {
    String message;
    int shift;

    Cipher(String message, int shift){
        this.message = message;
        this.shift = shift;
    }
    //USE MAPS, split(nothing) = splits every term and puts it into the list

    public String Encrypt(){
        shift;
        return "";
    }
    public String Decrypt(){
        - shift;
        return "";
    }
}




